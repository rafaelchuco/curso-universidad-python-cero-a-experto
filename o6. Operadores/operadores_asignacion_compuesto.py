print("***Operadores de asignacion compuesto***")
a,b = 10,15
print(f"El valor inicial de a es {a} y el valor inicial de b es {b}")

# Operador compuesto de suma +=
a += b
print(f"El valor final de a es {a} y el valor final de b es {b}")

# Operador compuesto de resta -=
a,b = 10,15
a -= b
print(f"El valor final de a es {a} y el valor final de b es {b}")

# Operador compuesto de multiplicación *=
a,b = 10,15
a *= b
print(f"El valor final de a es {a} y el valor final de b es {b}")

# Operador compuesto de división /=
a,b = 10,15
a /= b
print(f"El valor final de a es {a} y el valor final de b es {b}")