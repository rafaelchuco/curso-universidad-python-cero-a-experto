# Metodos de cadenas

cadena1 = "Hola, mundo"
print(f"Cadena original: {cadena1} ")

mayusculas = cadena1.upper() # Convierte a mayusculas
print(f"Cadena en mayusculas: {mayusculas}")

minusculas = cadena1.lower() # Convierte a minusculas
print(f"Cadena en minusculas: {minusculas}")

cadena2 = "  Adios, mundo  "
print(f"Cadena con espacios: {cadena2} ")

sin_espacios = cadena2.strip() # Elimina los espacios al principio y al final
print(f"Cadena sin espacios: {sin_espacios}")

